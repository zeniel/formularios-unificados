set DAT_PUBLICACAO = DAT_ATIVACAO_FORMULARIO,
USU_PUBLICACAO = USU_CRIACAO_QUESTIONARIO
WHERE DAT_PUBLICACAO IS NULL;


update questionario
set DSC_STATUS = 'PUBLICADO'
WHERE DAT_PUBLICACAO IS NOT NULL and DSC_STATUS = 'RASCUNHO';


